// Dart Control flow: Loops
void main() {
  /// A `while` loop evaluates the condition [before] the loop:
  /// ```dart
  /// while (!isDone()) {
  ///   doSomething();
  /// }
  /// ```
  
  /// A do-while loop evaluates the condition [after] the loop:
  /// ```dart
  /// do {
  ///   printLine();
  /// } while (!atEndOfPage());
  /// ```
  print('Hello, Dart!');
}
