// Operators in Dart
void main() {
  /* 
    * == : Equal; see discussion below
    * != : Not equal
    * >  : Greater than
    * <  : Less than
    * >= : Greater than or equal to
    * <= : Less than or equal to
  */

  /// `assert(condition)` throws an exception 
  /// if condition is false.
  
  assert(2 == 2);
  assert(2 != 3);
  assert(3 > 2);
  assert(2 < 3);
  assert(3 >= 3);
  assert(2 <= 3);

  print('Hello, Dart!');
}
